package com.CZ2002.enums;

/**
 * The current console state that the user is on
 */
public enum MenuView {
    /**
     * Previous console state
     */
    PREVIOUS_MENU,
    /**
     * Current console state
     */
    CURRENT_MENU,
    /**
     * Menu console state
     */
    MENU_ITEMS,
    /**
     * Sales Revenue console state
     */
    SALES_REVENUE,
    /**
     * Order console state
     */
    ORDERS,
    /**
     * Reservation console state
     */
    RESERVATIONS,
    /**
     * End of program state
     */
    PROGRAM_END
}
